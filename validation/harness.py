    import argparse
    import sys

    # Table content includes non-ASCII characters (e.g. the arrow in "batch=1
    # -> batch=64"). Windows consoles default to a legacy codepage (cp1252)
    # that can't encode them, crashing the print below with a raw
    # UnicodeEncodeError after the tables were already written successfully
    # to disk. Reconfigure stdout to UTF-8 first so console output matches
    # what's actually on disk; falls back silently on Python < 3.7 or a
    # stream that doesn't support reconfigure (e.g. captured/piped output).
    try:
        sys.stdout.reconfigure(encoding="utf-8", errors="replace")
    except (AttributeError, ValueError):
        pass

    ap = argparse.ArgumentParser(description="Generate the 4 validation tables.")
    ap.add_argument("--validation-dir", default="validation")
    ap.add_argument("--out-dir", default="validation/tables")
    args = ap.parse_args()
    tables = build_all_tables(args.validation_dir, args.out_dir)
    print(f"Wrote 4 tables to {args.out_dir}/")
    print("\n" + tables["killer_chart"])
